#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	a;

	a = 0;
	if (s == NULL || f == NULL)
		return ;
	while (s[a] != '\0')
	{
		f(&s[a]);
		a++;
	}
}
