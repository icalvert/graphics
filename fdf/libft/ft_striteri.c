#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	a;

	if (s == NULL || f == NULL)
		return ;
	a = 0;
	while (s[a] != '\0')
	{
		f(a, &s[a]);
		a++;
	}
}
